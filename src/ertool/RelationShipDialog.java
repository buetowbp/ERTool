    public RelationShipDialog() {
        initComponents();
        this.setTitle("New Relationship");
        for (Entity e: ERToolView.entities){
            EntityChoice1.add(e.getText());
        }
        for (Entity e: ERToolView.entities){
                   if (!EntityChoice1.getSelectedItem().equals(e.getText())){
                       EntityChoice2.add(e.getText());
                   }
               }
       
        EntityChoice1.addItemListener(new ItemListener() {

            ///when a different first entity is selected, make sure that it can't also be selected as the second entity
            
            public void itemStateChanged(ItemEvent arg0) {
               EntityChoice2.removeAll();
               for (Entity e: ERToolView.entities){
                   if (!EntityChoice1.getSelectedItem().equals(e.getText())){
                       EntityChoice2.add(e.getText());
                   }
               }
            }
        });
        
        initializeConstraints();
    }
    
    public RelationShipDialog(Relationship r) {
        initComponents();
        this.relationship = r;
        this.positionCenter(ERToolView.frame);
        this.setTitle("Edit Relationship");
        for (Entity e: ERToolView.entities){
            EntityChoice1.add(e.getText());
        }
        this.RelationshipNameField.setText(this.relationship.getText());
        this.EntityChoice1.select(this.relationship.firstEntity.getText());
        
        for (Entity e: ERToolView.entities){
                   if (!EntityChoice1.getSelectedItem().equals(e.getText())){
                       EntityChoice2.add(e.getText());
                   }
               }
        this.EntityChoice2.select(this.relationship.secondEntity.getText());
        
        EntityChoice1.addItemListener(new ItemListener() {

            ///when a different first entity is selected, make sure that it can't also be selected as the second entity
            
            public void itemStateChanged(ItemEvent arg0) {
               EntityChoice2.removeAll();
               for (Entity e: ERToolView.entities){
                   if (!EntityChoice1.getSelectedItem().equals(e.getText())){
                       EntityChoice2.add(e.getText());
                   }
               }
            }
        });
        
        initializeConstraints();
        EntityConstraintChoice1.select(this.relationship.mLink.getFirstConstraint().getName());
        EntityConstraintChoice2.select(this.relationship.mLink.getSecondConstraint().getName());
    }
    
    public void initializeConstraints() {
    	EntityConstraintChoice1.add(ERStore.rOne);
        EntityConstraintChoice1.add(ERStore.rZeroToOne);
        EntityConstraintChoice1.add(ERStore.rZeroToMany);
        EntityConstraintChoice1.add(ERStore.rOneToMany);
        
        EntityConstraintChoice2.add(ERStore.rOne);
        EntityConstraintChoice2.add(ERStore.rZeroToOne);
        EntityConstraintChoice2.add(ERStore.rZeroToMany);
        EntityConstraintChoice2.add(ERStore.rOneToMany);
    }

